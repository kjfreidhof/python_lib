# About: A Simple program to solve the square root of a number 

# Define the function named it main
def main():
    # Ask for user input and store it in the varriable
    n = eval(input("Enter a number: "))
    # Then solve the square root of the number 
    sqrt = n ** 0.5 
    # Then print out the answer
    print("The square root is", sqrt)

# Execute the function
main()


